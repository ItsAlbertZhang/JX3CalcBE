function Macro0(player)
    local sq;
    if m == 0 then
        sq = skillQueueQishou;
    elseif m == 1 then
        sq = skillQueue2;
    elseif m == 2 then
        sq = skillQueue3;
    elseif m == 3 then
        sq = skillQueue1;
    elseif m == 4 then
        sq = skillQueue2;
    elseif m == 5 then
        sq = skillQueue3;
    elseif m == 6 then
        sq = skillQueueShouwei;
    end;
    if skillDict[sq[i]] == 0 then -- 点掉诛邪
        player:buffDelMultiGroupByID(9909);
        i = i + 1;
    elseif skillDict[sq[i]] == 1 then -- 特效腰坠
        player:itemUse(1, 38789);
        i = i + 1;
    end;
    local id = skillDict[sq[i]];
    if player:cast(id) then
        i = i + 1; -- idx 此时为下一个技能的索引
        -- 处理下一个技能是隐身的情况
        if skillDict[sq[i]] == 3974 then
            player.delayCustom = 400;
        end;
        if i > #sq then
            i = 1;
            m = m + 1;
        end;
    end;
end;
